import math
a = int(input("a: "))
b = int(input("b: "))

print("Sum: " + str(a + b))
print("Difference: " + str(a - b))
print("Product: " + str(a * b))
print("Quotient: " + str(a // b))
print("Remainder: " + str(a % b))
print("Result of log10 a: " + str(math.log10(a)))
print("Result a^b: " + str(a ** b))
